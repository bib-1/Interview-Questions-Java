package String;

public class TextBlock {
    public static void main(String[] args) {
        String s = """
                Hello
                Hi
                Bonjour
                Namaste
                Hola
                """;

        String s2 = "Hello\nHi\nBonjour\nNamaste\nHola";

        System.out.println(s);
        System.out.println(s2);
    }
}

